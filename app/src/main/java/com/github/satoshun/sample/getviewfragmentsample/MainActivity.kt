class CustomView @JvmOverloads constructor(
    context: Context,
    attrs: AttributeSet? = null,
    defStyle: Int = 0
) : View(context, attrs, defStyle) {
  override fun onAttachedToWindow() {
    super.onAttachedToWindow()
    Log.d("From CustomView", findAttachFragment().toString())
  }
}
